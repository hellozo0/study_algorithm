	private static int BFS(int start){
		Queue<Integer> queue = new LinkedList<>();
		queue.add(start);
		visited[start] = 1; // RED
		int red = 1;
		int black = 0;
		boolean hasOddCycle = false; // ğŸš¨ ì‚¬ì´í´ì´ ìˆëŠ”ì§€ íŒë‹¨!!!!

		while (!queue.isEmpty()){
			int now = queue.poll();
			for (int next : array[now]) { //ìƒê°í•´ë³´ë‹ˆê¹Œ JAVA8ì´ë¼ì„œ ì´ê±° ë™ì‘ ì•ˆí–ˆì—ˆìŒ ê·¸ë˜ì„œ ì¼ë°˜ forë¬¸ìœ¼ë¡œ ë°”ê¿”ì„œ í’€ì—ˆìŒ
				if(visited[next] == 0) {
					visited[next] = -visited[now]; //ë°˜ëŒ€ ì»¬ëŸ¬
					queue.add(next);

					if(visited[next] == 1) red++;
					else black++;
				}else if (visited[next] == visited[now]) {
					// ğŸš¨ í™€ìˆ˜ ê¸¸ì´ ì‚¬ì´í´ ë°œìƒ!
					hasOddCycle = true;
				}
			}
		}

		// ğŸš¨ í™€ìˆ˜ ê¸¸ì´ ì‚¬ì´í´ì´ ìˆìœ¼ë©´ í•œ ê°œë¥¼ ì œê±°í•´ì•¼ í•¨.
		if (hasOddCycle) {
			return Math.max(red, black) - 1;
		} else {
			return Math.max(red, black);
		}
	}
